package svl.pos.dominio;


public class TipoServicio {

	private int tise_id;
	private String tise_descrico;
	private String tise_descrila;
	

	public TipoServicio(){
		super();
	}


	/**
	 * @return the tise_id
	 */
	public int getTise_id() {
		return tise_id;
	}


	/**
	 * @param tise_id the tise_id to set
	 */
	public void setTise_id(int tise_id) {
		this.tise_id = tise_id;
	}


	/**
	 * @return the tise_descrico
	 */
	public String getTise_descrico() {
		return tise_descrico;
	}


	/**
	 * @param tise_descrico the tise_descrico to set
	 */
	public void setTise_descrico(String tise_descrico) {
		this.tise_descrico = tise_descrico;
	}


	/**
	 * @return the tise_descrila
	 */
	public String getTise_descrila() {
		return tise_descrila;
	}


	/**
	 * @param tise_descrila the tise_descrila to set
	 */
	public void setTise_descrila(String tise_descrila) {
		this.tise_descrila = tise_descrila;
	}


	
	

	

	
}