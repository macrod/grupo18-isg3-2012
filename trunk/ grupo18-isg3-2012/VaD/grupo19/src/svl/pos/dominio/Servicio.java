public class Servicio {

	private int serv_id;
	private int tise_tiseid;
	private String serv_descrico;
	private String serv_descrila;
	

	public Servicio(){
		super();
	}


	/**
	 * @return the serv_id
	 */
	public int getServ_id() {
		return serv_id;
	}


	/**
	 * @param serv_id the serv_id to set
	 */
	public void setServ_id(int serv_id) {
		this.serv_id = serv_id;
	}


	/**
	 * @return the tise_tiseid
	 */
	public int getTise_tiseid() {
		return tise_tiseid;
	}


	/**
	 * @param tise_tiseid the tise_tiseid to set
	 */
	public void setTise_tiseid(int tise_tiseid) {
		this.tise_tiseid = tise_tiseid;
	}


	/**
	 * @return the serv_descrico
	 */
	public String getServ_descrico() {
		return serv_descrico;
	}


	/**
	 * @param serv_descrico the serv_descrico to set
	 */
	public void setServ_descrico(String serv_descrico) {
		this.serv_descrico = serv_descrico;
	}


	/**
	 * @return the serv_descrila
	 */
	public String getServ_descrila() {
		return serv_descrila;
	}


	/**
	 * @param serv_descrila the serv_descrila to set
	 */
	public void setServ_descrila(String serv_descrila) {
		this.serv_descrila = serv_descrila;
	}
	
	
	

	

	
}