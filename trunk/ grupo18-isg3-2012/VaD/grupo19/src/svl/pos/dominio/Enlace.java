package svl.pos.dominio;


public class Enlace {

	private String enla_id;
	private String enla_descrico;
	private String enla_descrila;
	private String enla_url;
	

	public Enlace(){
		super();
	}


	/**
	 * @return the enla_id
	 */
	public String getEnla_id() {
		return enla_id;
	}


	/**
	 * @param enla_id the enla_id to set
	 */
	public void setEnla_id(String enla_id) {
		this.enla_id = enla_id;
	}


	/**
	 * @return the enla_descrico
	 */
	public String getEnla_descrico() {
		return enla_descrico;
	}


	/**
	 * @param enla_descrico the enla_descrico to set
	 */
	public void setEnla_descrico(String enla_descrico) {
		this.enla_descrico = enla_descrico;
	}


	/**
	 * @return the enla_descrila
	 */
	public String getEnla_descrila() {
		return enla_descrila;
	}


	/**
	 * @param enla_descrila the enla_descrila to set
	 */
	public void setEnla_descrila(String enla_descrila) {
		this.enla_descrila = enla_descrila;
	}


	/**
	 * @return the enla_url
	 */
	public String getEnla_url() {
		return enla_url;
	}


	/**
	 * @param enla_url the enla_url to set
	 */
	public void setEnla_url(String enla_url) {
		this.enla_url = enla_url;
	}


	
	

	

	
}