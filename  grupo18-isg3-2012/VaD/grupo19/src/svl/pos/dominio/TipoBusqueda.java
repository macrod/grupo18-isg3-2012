package svl.pos.dominio;

public class TipoBusqueda {

	private String tibu_id;
	private String titu_descrico;
	
	
	public TipoBusqueda() {
		super();
	}


	/**
	 * @return the tibu_id
	 */
	public String getTibu_id() {
		return tibu_id;
	}


	/**
	 * @param tibu_id the tibu_id to set
	 */
	public void setTibu_id(String tibu_id) {
		this.tibu_id = tibu_id;
	}


	/**
	 * @return the titu_descrico
	 */
	public String getTitu_descrico() {
		return titu_descrico;
	}


	/**
	 * @param titu_descrico the titu_descrico to set
	 */
	public void setTitu_descrico(String titu_descrico) {
		this.titu_descrico = titu_descrico;
	}

	
}
