package svl.pos.dominio;


public class Perfil {

	private String perf_id;
	private String perf_descrico;
	private String perf_descrila;
	

	public Perfil(){
		super();
	}


	/**
	 * @return the perf_id
	 */
	public String getPerf_id() {
		return perf_id;
	}


	/**
	 * @param perf_id the perf_id to set
	 */
	public void setPerf_id(String perf_id) {
		this.perf_id = perf_id;
	}


	/**
	 * @return the perf_descrico
	 */
	public String getPerf_descrico() {
		return perf_descrico;
	}


	/**
	 * @param perf_descrico the perf_descrico to set
	 */
	public void setPerf_descrico(String perf_descrico) {
		this.perf_descrico = perf_descrico;
	}


	/**
	 * @return the perf_descrila
	 */
	public String getPerf_descrila() {
		return perf_descrila;
	}


	/**
	 * @param perf_descrila the perf_descrila to set
	 */
	public void setPerf_descrila(String perf_descrila) {
		this.perf_descrila = perf_descrila;
	}


	
	

	

	
}